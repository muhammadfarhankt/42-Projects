#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*newlst;
	t_list	*newnode;
	void	*newcont;

	newlst = NULL;
	if (!lst)
		return (NULL);
	while (lst)
	{
		newcont = (*f)(lst->content);
		newnode = ft_lstnew(newcont);
		if (!newnode)
		{
			(*del)(newcont);
			ft_lstclear(&newlst, del);
			return (NULL);
		}
		ft_lstadd_back(&newlst, newnode);
		lst = lst->next;
	}
	return (newlst);
}
